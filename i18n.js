document.addEventListener('DOMContentLoaded', () => {
    const langButtons = document.querySelectorAll('[data-lang]');

    // Check local storage or browser language
    let currentLang = localStorage.getItem('babyland_lang') || 'en';

    // Validate if saved lang is supported
    if (!['en', 'es', 'pt'].includes(currentLang)) {
        currentLang = 'en';
    }

    // Apply initial translation
    setLanguage(currentLang);

    langButtons.forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.getAttribute('data-lang');
            setLanguage(lang);
        });
    });
});

function setLanguage(lang) {
    // Save preference
    localStorage.setItem('babyland_lang', lang);

    // Update active state of buttons
    document.querySelectorAll('[data-lang]').forEach(btn => {
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });

    // Update texts
    const textsToUpdate = document.querySelectorAll('[data-i18n]');
    textsToUpdate.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key]; // Using innerHTML allowing embedded icons or tags if we needed them, though textContent is safer. Kept innerHTML since icons can be inside buttons etc.
        }
    });

    // Special cases: placeholders
    document.querySelectorAll('input[type="text"], input[type="email"], textarea').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (key && translations[lang] && translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });
}
